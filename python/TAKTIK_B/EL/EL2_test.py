from EL2 import solve

def test_document1():
    maze = [
'########### #########',
'# #     #   #   #   #',
'# # # ### ##### # ###',
'# # # #   #     #   #',
'# # ### ### ### ### #',
'#         # #   #   #',
'##### ### # # ### # #',
'#     # # # #     # #',
'# ### # # # ####### #',
'#   #   #   #   #   #',
'# # ######### # # ###',
'# # # #     # #   # #',
'# # # # ### # ##### #',
'# # # #   #   #   # #',
'##### ### ### # ### #',
'#     #   # #       #',
'# ##### # # ####### #',
'#   #   # #   #     #',
'### # ### ### # ### #',
'#       # ###   #   #',
'######### ###########'
]
    sol = [
'###########.#########',
'# #     #...#   #   #',
'# # # ###.##### # ###',
'# # # #...#.....#   #',
'# # ###.###.###.### #',
'#.........#.#...#...#',
'#####.###.#.#.###.#.#',
'#.....# #.#.#.....#.#',
'#.###.# #.#.#######.#',
'#...#...#...#...#...#',
'#.#.#########.#.#.###',
'#.#.# #.....#.#...# #',
'#.#.# #.###.#.##### #',
'#.#.# #...#...#   # #',
'##### ###.###.# ### #',
'#     #...# #.......#',
'# #####.#.# #######.#',
'#   #...#.#   #.....#',
'### #.###.### #.###.#',
'#.......#.###...#...#',
'#########.###########'
]
    assert(solve(maze) == sol)

